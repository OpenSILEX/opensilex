<template>
  <b-form-group :required="!uriGenerated">
    <ValidationProvider :vid="id">
      <b-form-checkbox v-if="!editMode" :id="id" v-model="uriGenerated" :name="id">
        <opensilex-FormInputLabelHelper
                :label="label"
                :helpMessage="helpMessage"
                :labelFor="'field' + id">
        </opensilex-FormInputLabelHelper>
        <!-- helpMessage="component.common.uri.help-message" -->
      </b-form-checkbox>
      <opensilex-FormInputLabelHelper
              v-else
              :label="label"
              :helpMessage="helpMessage"
              :labelFor="'field' + id"
              class="checkbox">

      </opensilex-FormInputLabelHelper>
    </ValidationProvider>
    <ValidationProvider
      :name="$t(label)"
      :rules="uriRules"
      v-slot="{ errors }"
    >
      <div v-bind:class="{ errors: errors.length > 0 }">
        <b-form-input
          :id="'field' + id"
          v-model="valueURI"
          :disabled="uriGenerated"
          type="text"
          required
          :placeholder="placeholder ? $t(placeholder) : $t('component.common.autogenerated-uri')"
        ></b-form-input>
      </div>
      <div class="error-message alert alert-danger">{{ errors[0] }}</div>
    </ValidationProvider>
  </b-form-group>
</template>

<script lang="ts">
import { Component, Prop, PropSync } from "vue-property-decorator";
import Vue from "vue";

@Component
export default class UriForm extends Vue {
  $opensilex: any;

  @PropSync("uri")
  valueURI!: string;

  @Prop()
  editMode!: boolean;

  @Prop()
  label!: string;

  @Prop()
  helpMessage!: string;

  @Prop()
  placeholder: string;

  @Prop({ default: false })
  required!: boolean;

  @PropSync("generated")
  uriGenerated!: boolean;

  id!: string;

  created() {
    this.id = this.$opensilex.generateID();
  }

  // règles vee-validate adaptées
  get uriRules(): string {
    // si le parent est "required", on force required
    if (this.required) {
      return "required|no_spaces|url";
    }
    // sinon on garde la logique interne existante
    return `required_if:${this.id},false|no_spaces|url`;
  }
}
</script>

