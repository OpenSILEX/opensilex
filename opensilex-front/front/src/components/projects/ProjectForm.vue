            <b-form-group :label="$t('component.project.uri') + ':'" label-for="uri">
              <ValidationProvider vid="autogenerated">
                <b-form-checkbox
                  v-if="!editMode"
                  id="autogenerated"
                  v-model="uriGenerated"
                  name="autogenerated"
                >{{$t('component.common.autogenerated-uri')}}</b-form-checkbox>
              </ValidationProvider>
              <ValidationProvider
                name="uri"
                rules="required_if:autogenerated,false|url"
                v-slot="{ errors }"
              >
                <b-form-input
                  id="uri"
                  v-model="form.uri"
                  :disabled="uriGenerated"
                  type="text"
                  required
                  :placeholder="$t('component.common.autogenerated-uri')"
                ></b-form-input>
                <div class="error-message alert alert-danger">{{ errors[0] }}</div>
              </ValidationProvider>
            </b-form-group>

            <div class="row">
              <div class="col-lg-6">
                <!-- Name -->
                <b-form-group
                  :label="$t('component.project.name') + ':'"
                  label-for="name"
                  required
                  label-class="required"
                >
                  <ValidationProvider
                    :name="$t('component.common.name')"
                    rules="required"
                    v-slot="{ errors }"
                  >
                    <b-form-input
                      id="name"
                      v-model="form.label"
                      type="text"
                      required
                      :placeholder="$t('component.project.form-name-placeholder')"
                    ></b-form-input>

                    <div class="error-message alert alert-danger">{{ errors[0] }}</div>
                  </ValidationProvider>
                </b-form-group>
              </div>
              <!-- ShortName  -->
              <div class="col-lg-6">
                <b-form-group :label="$t('component.common.acronym') + ':'" label-for="acronym">
                  <b-form-input
                    id="acronym"
                    v-model="form.shortname"
                    type="text"
                    :placeholder="$t('component.project.form-acronym-placeholder')"
                  ></b-form-input>
                </b-form-group>
              </div>
            </div>
            <!-- Period -->
            <div class="row">
              <!-- StartDate -->
              <div class="col-lg-6">
                <b-form-group
                  required
                  :label="$t('component.experiment.startDate') + ':'"
                  label-for="startDate"
                >
                  <ValidationProvider
                    :name="$t('component.experiment.startDate')"
                    rules="required"
                    v-slot="{ errors }"
                  >
                    <b-form-input
                      id="startDate"
                      v-model="form.startDate"
                      type="date"
                      value="2020-03-05"
                      required
                    ></b-form-input>
                    <div class="error-message alert alert-danger">{{ errors[0] }}</div>
                  </ValidationProvider>
                </b-form-group>
              </div>
              <!-- EndDate -->
              <div class="col-lg-6">
                <b-form-group :label="$t('component.experiment.endDate') + ':'" label-for="endDate">
                  <ValidationProvider
                    :name="$t('component.experiment.endDate')"
                    v-slot="{ errors }"
                  >
                    <b-form-input
                      id="endDate"
                      v-model="form.endDate"
                      type="date"
                      value="2020-03-05"
                    ></b-form-input>
                    <div class="error-message alert alert-danger">{{ errors[0] }}</div>
                  </ValidationProvider>
                </b-form-group>
              </div>
            </div>

            <!-- Objective -->
            <b-form-group :label="$t('component.project.objective') + ':'" label-for="objective">
              <b-form-input
                id="objective"
                v-model="form.objective"
                type="text"
                :placeholder="$t('component.project.form-objective-placeholder')"
              ></b-form-input>
            </b-form-group>

            <!-- Description -->
            <b-form-group
              :label="$t('component.project.description') + ':'"
              label-for="description"
            >
              <b-form-textarea
                id="description"
                v-model="form.description"
                :placeholder="$t('component.project.form-description-placeholder')"
                autocomplete="no"
                rows="3"
              ></b-form-textarea>
            </b-form-group>
