<template>
    <ValidationObserver ref="validatorRef">

        <p>
            <b> {{ $t("Move.location") }}</b>
        </p>
        <hr/>

        <div class="row">
            <div class="col-lg-5">
                <opensilex-FacilitySelector
                        ref="moveFromSelector"
                        label="Position.from"
                        :facilities.sync="form.from"
                        :multiple="false"
                        :required="fromRequired"
                        @select="updateRequiredProps('moveFromSelector')"
                        @clear="updateRequiredProps('moveFromSelector')"
                        helpMessage="Position.from-help"
                ></opensilex-FacilitySelector>
            </div>
            <div class="col-lg-5">
                <opensilex-FacilitySelector
                        ref="moveToSelector"
                        label="Position.to"
                        :facilities.sync="form.to"
                        :multiple="false"
                        :required="toRequired"
                        @select="updateRequiredProps('moveToSelector')"
                        @clear="updateRequiredProps('moveToSelector')"
                        helpMessage="Position.to-help"
                ></opensilex-FacilitySelector>
            </div>
        </div>

        <div>
            <p><b> {{ $t("Position.title") }}</b></p>
            <hr/>
            <opensilex-PositionForm v-if="form.targets_positions && form.targets_positions[0]"
               :form.sync="form.targets_positions[0].position">
            </opensilex-PositionForm>
        </div>

    </ValidationObserver>
</template>

<script lang="ts">
    import {Component, Prop, Ref} from "vue-property-decorator";
    import Vue from "vue";
    import PositionForm from "../../positions/form/PositionForm.vue";
    import { MoveCreationDTO, TargetPositionCreationDTO } from 'opensilex-core/index';

    @Component
    export default class MoveForm extends Vue {

        @Ref("validatorRef") readonly validatorRef!: any;

        editMode = false;
        fromRequired: boolean = true;
        toRequired: boolean = true;

        @Prop({default: () => MoveForm.getEmptyForm()})
        form: MoveCreationDTO;

        static getEmptyTargetsPositions() : Array<TargetPositionCreationDTO> {
            return  [{
                    target: undefined,
                    position : PositionForm.getEmptyForm()
                }];
         }

        static getEmptyForm() : MoveCreationDTO{
            return {
              uri: undefined,
              rdf_type: undefined,
              start: undefined,
              end: undefined,
              is_instant: true,
              description: undefined,
              targets: [],

              relations: [],

              // move specific properties
                from: undefined,
                to: undefined,

                // move position(s)
              targets_positions: MoveForm.getEmptyTargetsPositions()
            };
        }

        getEmptyForm() {
            return MoveForm.getEmptyForm();
        }

        created(){
        }


        reset() {
            return this.validatorRef.reset();
        }

        validate() {
            return this.validatorRef.validate();
        }

        /**
         * We want at least one of the two fields "To" and "From" not to be empty then they are defined as required. 
         * From the moment one is completed, the other is no longer indicated as required
         */
        updateRequiredProps(ref) {
            if(this.form.from == undefined && this.form.to == undefined) {
                this.fromRequired = true;
                this.toRequired = true; 
            } else {
                if (this.form.from !== undefined && this.form.to == undefined) {
                    this.fromRequired = true;
                    this.toRequired = false;
                } else {
                    if (this.form.to !== undefined && this.form.from == undefined) {
                        this.toRequired = true;
                        this.fromRequired = false;
                    } else {
                        this.fromRequired = true;
                        this.toRequired = true;
                    }
                }
            }
        }
    }
</script>

