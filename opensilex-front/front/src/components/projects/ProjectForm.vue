<script lang="ts">
import { Component, Prop, Ref } from "vue-property-decorator";
import Vue from "vue";
import VueRouter from "vue-router";
import { ProjectGetDTO } from "opensilex-core/index";
import { SecurityService, UserGetDTO } from "opensilex-security/index";
import HttpResponse, {
  OpenSilexResponse
} from "opensilex-security/HttpResponse";

@Component
export default class ProjectForm extends Vue {
  $opensilex: any;
  $store: any;
  $router: VueRouter;
  $i18n: any;

  @Ref("wizardRef") readonly wizardRef!: any;
  @Ref("validatorRef") readonly validatorRef!: any;
  @Ref("administrativeContactsRef") readonly administrativeContactsRef!: any;
  @Ref("scientificContactsRef") readonly scientificContactsRef!: any;
  @Ref("coordinatorsRef") readonly coordinatorsRef!: any;
  @Ref("formRef") readonly formRef!: any;

  get user() {
    return this.$store.state.user;
  }

  uriGenerated = true;

  form: ProjectGetDTO = {
    uri: "",
    label: "",
    shortname: "",
    description: "",
    objective: "",
    startDate: "",
    endDate: "",
    keywords: [],
    homePage: "",
    experiments: [],
    administrativeContacts: [],
    coordinators: [],
    scientificContacts: [],
    relatedProjects: []
  };

  title = "";

  editMode = false;

  clearForm() {
    this.form = {
      uri: "",
      label: "",
      shortname: undefined,
      description: undefined,
      objective: undefined,
      startDate: undefined,
      endDate: undefined,
      keywords: undefined,
      homePage: undefined,
      experiments: undefined,
      administrativeContacts: undefined,
      coordinators: undefined,
      scientificContacts: undefined,
      relatedProjects: undefined
    };
  }
  onScContactsSelected(value) {
    this.form.scientificContacts = value;
  }

  onAdminContactsSelected(value) {
    this.form.administrativeContacts = value;
  }

  showCreateForm() {
    this.clearForm();
    this.form.startDate = undefined;
    this.form.endDate = undefined;
    this.editMode = false;
    this.title = this.$i18n.t("component.project.add").toString();
    this.uriGenerated = true;
    let modalRef: any = this.$refs.modalRef;
    modalRef.show();
    this.validatorRef.reset();
    this.administrativeContactsRef.selectUsers(
      this.form.administrativeContacts
    );
    this.scientificContactsRef.selectUsers(this.form.scientificContacts);
    this.coordinatorsRef.selectUsers(this.form.coordinators);

    this.wizardRef.reset();
  }

  showEditForm(form: ProjectGetDTO) {
    this.form = form;
    this.editMode = true;
    this.title = this.$i18n.t("component.project.update").toString();
    this.uriGenerated = true;
    let modalRef: any = this.$refs.modalRef;
    modalRef.show();
    this.administrativeContactsRef.selectUsers(
      this.form.administrativeContacts
    );
    this.scientificContactsRef.selectUsers(this.form.scientificContacts);
    this.coordinatorsRef.selectUsers(this.form.coordinators);
    this.validatorRef.reset();
    this.wizardRef.tabs.forEach(tab => {
      tab.checked = true;
    });

    this.wizardRef.navigateToTab(0);
  }

  hideForm() {
    let modalRef: any = this.$refs.modalRef;
    modalRef.hide();
  }

  updateAdminContacts(contacts) {
    this.form.administrativeContacts = contacts;
  }
  updateScientificContacts(contacts) {
    this.form.scientificContacts = contacts;
  }
  updateCoordinators(contacts) {
    this.form.coordinators = contacts;
  }

  onValidate() {
    return new Promise((resolve, reject) => {
      if (this.editMode) {
        this.$emit("onUpdate", this.form, result => {
          if (result instanceof Promise) {
            result.then(resolve).catch(reject);
          } else {
            resolve(result);
          }
        });
      } else {
        return this.$emit("onCreate", this.form, result => {
          if (result instanceof Promise) {
            result.then(resolve).catch(reject);
          } else {
            resolve(result);
          }
        });
      }
    });
  }

  validateStep(props) {
    this.validatorRef.validate().then(isValid => {
      console.error(isValid);
      if (isValid) {
        props.nextTab();
      }
    });
  }

  validate() {
    if (this.uriGenerated && !this.editMode) {
      this.form.uri = undefined;
    }
    if (this.formRef.checkValidity()) {
      if (this.uriGenerated && !this.editMode) {
        this.form.uri = undefined;
      }
      this.onValidate()
        .then(() => {
          this.$nextTick(() => {
            let modalRef: any = this.$refs.modalRef;
            modalRef.hide();
          });
        })
        .catch(error => {
          if (error.status == 409) {
            // TODO display error message project already exists
            console.error("TODO display error message project already exists");
          } else {
            this.$opensilex.errorHandler(error);
          }
        });
    } else {
      // TODO validation error management
      console.error("TODO validation error management");
    }
  }
}
</script>

